  let currentLang = localStorage.getItem("lang") || "en";

  function setMetaFromTranslations(lang) {
    const title = translations[lang]?.page_title;
    const desc = translations[lang]?.page_desc;
    if (title) document.title = title;
    const meta = document.getElementById("metaDescription");
    if (meta && desc) meta.setAttribute("content", desc);
  }

  function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem("lang", lang);

    document.querySelectorAll("[data-i18n]").forEach((el) => {
      const key = el.getAttribute("data-i18n");
      const value = translations[lang]?.[key];
      if (typeof value === "string") el.textContent = value;
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
      const key = el.getAttribute("data-i18n-placeholder");
      const value = translations[lang]?.[key];
      if (typeof value === "string") el.setAttribute("placeholder", value);
    });

    document.documentElement.lang = lang;
    document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
    setMetaFromTranslations(lang);
  }

  function buildWhatsAppUrl(message) {
    const base = `https://wa.me/${WHATSAPP_NUMBER}`;
    const text = (message || "").trim();
    if (!text) return base;
    return `${base}?text=${encodeURIComponent(text)}`;
  }

  function initMobileNav() {
    const toggle = document.getElementById("mobileMenuToggle");
    const nav = document.getElementById("mainNav");
    if (!toggle || !nav) return;

    function setExpanded(isExpanded) {
      toggle.setAttribute("aria-expanded", String(isExpanded));
      nav.classList.toggle("nav--open", isExpanded);
      const icon = toggle.querySelector(".material-symbols-outlined");
      if (icon) icon.textContent = isExpanded ? "close" : "menu";
    }

    toggle.addEventListener("click", () => {
      const isOpen = nav.classList.contains("nav--open");
      setExpanded(!isOpen);
    });

    nav.addEventListener("click", (e) => {
      const target = e.target;
      if (!(target instanceof Element)) return;
      const isLink = target.closest("a");
      const isBtn = target.closest("button");
      if (isLink || isBtn) setExpanded(false);
    });

    window.addEventListener("resize", () => {
      if (window.innerWidth > 860) setExpanded(false);
    });
  }

  function initReveal() {
    const els = Array.from(document.querySelectorAll(".reveal"));
    if (!els.length) return;

    const reduce = window.matchMedia && window.matchMedia("(prefers-reduced-motion: reduce)").matches;
    if (reduce) {
      els.forEach((el) => el.classList.add("is-visible"));
      return;
    }

    const observer = new IntersectionObserver(
      (entries, obs) => {
        entries.forEach((entry) => {
          if (!entry.isIntersecting) return;
          entry.target.classList.add("is-visible");
          obs.unobserve(entry.target);
        });
      },
      { threshold: 0.14, rootMargin: "0px 0px -40px 0px" }
    );

    els.forEach((el) => observer.observe(el));
  }

  function initHeaderBehavior() {
    const header = document.getElementById("siteHeader");
    if (!header) return;

    let lastY = window.scrollY || 0;
    const minHideY = 420;

    window.addEventListener("scroll", () => {
      const y = window.scrollY || 0;
      header.classList.toggle("header--scrolled", y > 10);
      header.classList.toggle("header--hidden", y > lastY && y > minHideY);
      lastY = y;
    });
  }

  function initQuoteForm() {
    const form = document.getElementById("quoteForm");
    const service = document.getElementById("quoteService");
    const message = document.getElementById("quoteMessage");
    if (!form || !service || !message) return;

    form.addEventListener("submit", (e) => {
      e.preventDefault();
      const serviceLabel = service.options[service.selectedIndex]?.textContent || "";
      const msg = (message.value || "").trim();
      const t = translations[currentLang] || translations.en;
      const full = `${t.wa_prefix}\n${t.wa_service_label}: ${serviceLabel}\n${t.wa_message_label}: ${msg || "-"}`;
      window.open(buildWhatsAppUrl(full), "_blank", "noopener");
    });
  }

  function initYear() {
    const year = document.getElementById("year");
    if (!year) return;
    year.textContent = String(new Date().getFullYear());
  }

  document.getElementById("langToggle")?.addEventListener("click", () => {
    const next = currentLang === "en" ? "ar" : "en";
    setLanguage(next);
  });

  setLanguage(currentLang);
